function parseEscapedStringToBuffer(escapedString) {
    // Remove escape characters and convert hex string to binary array
    let binaryString = unescape(escapedString);
    let len = binaryString.length;
    let bytes = new Uint8Array(new ArrayBuffer(len));
    for (let i = 0; i < len; i++) {
        bytes[i] = binaryString.charCodeAt(i);
    }
    return bytes.buffer;
}

